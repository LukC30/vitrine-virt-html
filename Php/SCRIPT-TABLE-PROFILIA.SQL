CREATE TABLE `PROFILIA` (
	`CODPROD` BIGINT(20) NOT NULL DEFAULT '0',
	`DESCRICAO` VARCHAR(120) NULL DEFAULT NULL COLLATE 'utf8_general_ci',
	`NUMFABRICANTE` VARCHAR(35) NULL DEFAULT NULL COLLATE 'utf8_general_ci',
	`PRVENDA` DOUBLE(15,3) NULL DEFAULT NULL,
	`UNIDADE` CHAR(15) NULL DEFAULT NULL COLLATE 'utf8_general_ci',
	`CODGRU` INT(10) NULL DEFAULT NULL,
	`OBS` LONGTEXT NULL DEFAULT NULL COLLATE 'utf8_general_ci',
	PRIMARY KEY (`CODPROD`) USING BTREE
)
COLLATE='utf8_general_ci'
ENGINE=MyISAM
ROW_FORMAT=DYNAMIC
AUTO_INCREMENT=1
MAX_ROWS=999999
;
