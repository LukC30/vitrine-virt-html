CREATE TABLE `grupo` (
	`CODGRU` INT(11) NOT NULL DEFAULT '0',
	`NOMEGRU` VARCHAR(200) NULL DEFAULT NULL COLLATE 'utf8_general_ci',
	`STATUS` CHAR(3) NOT NULL DEFAULT 'A' COLLATE 'utf8_general_ci',
	`CODDEP` INT(11) NOT NULL DEFAULT '0',
	PRIMARY KEY (`CODGRU`) USING BTREE,
	INDEX `CODGRU` (`CODGRU`) USING BTREE,
	INDEX `CODDEP` (`CODDEP`) USING BTREE,
	INDEX `NOMEGRU` (`NOMEGRU`) USING BTREE
)
COLLATE='utf8_general_ci'
ENGINE=MyISAM
ROW_FORMAT=DYNAMIC
AUTO_INCREMENT=1
MAX_ROWS=999999
;

